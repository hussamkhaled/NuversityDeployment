package avh.nuversity.lms.services.impl.response;

public class StudentContractSheet {

	private String name;
	private int general;
	private int major;
	private int core;
	private int elective;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getGeneral() {
		return general;
	}
	public void setGeneral(int general) {
		this.general = general;
	}
	public int getMajor() {
		return major;
	}
	public void setMajor(int major) {
		this.major = major;
	}
	public int getCore() {
		return core;
	}
	public void setCore(int core) {
		this.core = core;
	}
	public int getElective() {
		return elective;
	}
	public void setElective(int elective) {
		this.elective = elective;
	}
	
}
