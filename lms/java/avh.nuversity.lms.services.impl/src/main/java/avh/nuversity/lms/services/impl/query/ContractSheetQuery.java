package avh.nuversity.lms.services.impl.query;

public class ContractSheetQuery {

	private String major;
	private int coreCrd;
	private int majorCrd;
	private int electiveCrd;
	private int generalCrd;
	public String getMajor() {
		return major;
	}
	public void setMajor(String major) {
		this.major = major;
	}
	public int getCoreCrd() {
		return coreCrd;
	}
	public void setCoreCrd(int coreCrd) {
		this.coreCrd = coreCrd;
	}
	public int getMajorCrd() {
		return majorCrd;
	}
	public void setMajorCrd(int majorCrd) {
		this.majorCrd = majorCrd;
	}
	public int getElectiveCrd() {
		return electiveCrd;
	}
	public void setElectiveCrd(int electiveCrd) {
		this.electiveCrd = electiveCrd;
	}
	public int getGeneralCrd() {
		return generalCrd;
	}
	public void setGeneralCrd(int generalCrd) {
		this.generalCrd = generalCrd;
	}
}
